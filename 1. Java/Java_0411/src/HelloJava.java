
public class HelloJava {
	// 메인 함수: 프로그램 실행 시킬 때, 메인 함수에 있는 내용을 위주로 실행함
	public static void main(String args[]) {
		// 여기서 코드 작성하면 됌
		//; 세미콜론: 문장울 끝낸다
		// 전체 주석처리: 드래그->CTRL+/
		//한줄에 하나씩 출력, 다음줄로 넘어감
		System.out.println("println문 사용");
		// "\n" 담줄 넘어가서 출력, ln이랑 같음
		System.out.print("print문 사용\n");
		//그냥 붙어서 출력
		System.out.print("print문 사용");		
		// 출력문에서 + 는 이어붙이는 역할
		System.out.println("1+2="+(1+2));
	}
}
